"""D&D 5.5e SRD item data — weapons and armor."""

from __future__ import annotations

from dataclasses import dataclass

from game_engine.types import ArmorCategory, DamageType, DiceNotation, WeaponProperty


@dataclass(frozen=True)
class WeaponData:
    name: str
    damage_dice: DiceNotation
    damage_type: DamageType
    properties: list[WeaponProperty]
    range_normal_ft: int | None
    range_long_ft: int | None
    weight_lb: float
    cost_gp: float
    two_handed: bool


@dataclass(frozen=True)
class ArmorData:
    name: str
    armor_type: ArmorCategory
    base_ac: int
    dex_bonus: bool
    dex_cap: int | None
    min_strength: int
    stealth_disadvantage: bool
    weight_lb: float
    cost_gp: float


